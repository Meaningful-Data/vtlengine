SELECT
    CNTRCT_ID,
    DT_RFRNC,
    INSTRMNT_ID,
    PRTCTN_ID,
    OBSRVD_AGNT_CD,
    IS_PRTCTN_MTRTY_MSMTCH,
    CASE IS_PRTCTN_MTRTY_MSMTCH
        WHEN 'T'
            THEN julian(DATE(DT_LGL_FNL_MTRTY)) - julian(DATE(DT_MTRTY_PRTCTN))
    END AS MTRTY_MSMTCH,
    julian(DATE(DT_LGL_FNL_MTRTY)) - julian(DATE(DT_MTRTY_PRTCTN)) AS PRTCTN_RSDL_MTRTY_DYS
FROM MSMTCH_BL_DS;