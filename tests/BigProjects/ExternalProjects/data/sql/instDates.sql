    CNTRCT_ID,
    CRDTR_ID,
    DBTR_ID,
    DT_RFRNC,
    INSTRMNT_ID,
    OBSRVD_AGNT_CD,
    julian(DATE(DT_END_INTRST_ONLY)) - julian(DATE(DT_RFRNC)) AS NXT_INTRST_RT_RST_DYS,
    julian(DATE(DT_LGL_FNL_MTRTY)) - julian(DATE(DT_INCPTN)) AS ORGNL_MTRTY,
    julian(DATE(DT_LGL_FNL_MTRTY)) - julian(DATE(DT_RFRNC)) AS RSDL_MTRTY,
    julian(DATE(DT_RFRNC)) - julian(DATE(DT_PST_D)) AS PST_D_DYS
