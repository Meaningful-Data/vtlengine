SELECT 
  A.CNTRCT_ID,
  A.DT_RFRNC,
  ifnull(A.DBTR_ID, "N/A") as DBTR_ID, 
  A.INSTRMNT_ID, 
  A.OBSRVD_AGNT_ID, 
  A.JNT_LBLTY_AMNT, 
  ifnull(A.CRDTR_ID, "N/A") as CRDTR_ID, 
  ifnull(A.SRVCR_ID, "N/A") as SRVCR_ID
FROM 
  B01.INSTRMNT_FCT_4_TEMP as A 
  inner join B01.INSTRMNT_FCT_SRVCR as B 
    on 
      A.OBSRVD_AGNT_ID = B.OBSRVD_AGNT_ID AND
      A.DT_RFRNC = B.DT_RFRNC AND
      A.CNTRCT_ID = B.CNTRCT_ID AND
      A.INSTRMNT_ID = B.INSTRMNT_ID