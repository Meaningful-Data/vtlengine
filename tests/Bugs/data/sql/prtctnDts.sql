SELECT
    CNTRCT_ID,
    DT_RFRNC,
    INSTRMNT_ID,
    PRTCTN_ID,
    OBSRVD_AGNT_CD,
    IS_PRTCTN_MTRTY_MSMTCH,
    CASE IS_PRTCTN_MTRTY_MSMTCH
        WHEN 'T'
        THEN DATEDIFF(
            'day',
            CAST(DT_MTRTY_PRTCTN AS DATE),
            CAST(DT_LGL_FNL_MTRTY AS DATE)
        )
    END AS MTRTY_MSMTCH,
    DATEDIFF(
        'day',
        CAST(DT_MTRTY_PRTCTN AS DATE),
        CAST(DT_LGL_FNL_MTRTY AS DATE)
    ) AS PRTCTN_RSDL_MTRTY_DYS
FROM MSMTCH_BL_DS;
