SELECT
    CNTRCT_ID,
    DT_RFRNC,
    INSTRMNT_ID,
    PRTCTN_ID,
    OBSRVD_AGNT_CD,
    IS_PRTCTN_MTRTY_MSMTCH,
    CASE IS_PRTCTN_MTRTY_MSMTCH
        WHEN 'T'
        THEN CAST(julian(DT_LGL_FNL_MTRTY) - julian(DT_MTRTY_PRTCTN) AS DOUBLE)
    END AS MTRTY_MSMTCH,
    CAST(julian(DT_LGL_FNL_MTRTY) - julian(DT_MTRTY_PRTCTN) AS DOUBLE) AS PRTCTN_RSDL_MTRTY_DYS
FROM MSMTCH_BL_DS;
