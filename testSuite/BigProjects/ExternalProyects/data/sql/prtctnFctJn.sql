SELECT 
  A.OBSRVD_AGNT_CD, 
  A.DT_RFRNC, 
  ifnull(A.CNTRCT_ID, "N/A") as CNTRCT_ID, 
  ifnull(A.INSTRMNT_ID, "N/A") as INSTRMNT_ID, 
  A.PRTCTN_ID, 
  ifnull(B.PRTCTN_PRVDR_CD, "N/A") as PRTCTN_PRVDR_CD, 
  A.PRTCTN_ALLCTD_VL,
  A.THRD_PRTY_PRRTY_CLMS
FROM
  ANCRDT_INSTRMNT_PRTCTN_RCVD_C as A 
  inner join ANCRDT_PRTCTN_RCVD_C as B 
  on 
    A.OBSRVD_AGNT_CD = B.OBSRVD_AGNT_CD AND
    A.DT_RFRNC = B.DT_RFRNC AND
    A.PRTCTN_ID = B.PRTCTN_ID